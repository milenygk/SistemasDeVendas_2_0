    private void btnSalvar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar3ActionPerformed
        try{
            Televisao televisao = new Televisao();
        
        televisao.setNome(tfNome3.getText());
        televisao.setPreco(Double.parseDouble(tfPreco3.getText()));
        televisao.setPolegadas(tfPolegadas.getText());
        
        ControleSistema.produtos.add(televisao);
        
         JOptionPane.showMessageDialog(null, "Produto " + televisao.getNome() + " inserido com sucesso!");
        
        tfNome3.setText("");
        tfPreco3.setText("");
        tfPolegadas.setText("");
              
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Erro ao salvar!");
       }
       
       for(int i = 0; i < ControleSistema.produtos.size(); i++){
          System.out.println(ControleSistema.produtos.get(i));
       }                         
        
    }//GEN-LAST:event_btnSalvar3ActionPerformed

